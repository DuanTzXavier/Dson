public class Friend {
    private String aaa;
    private float bb;
    private int ca;
    private double ba;
    private boolean ao;

    public String getAaa() {
        return aaa;
    }

    public void setAaa(String aaa) {
        this.aaa = aaa;
    }

    public float getBb() {
        return bb;
    }

    public void setBb(float bb) {
        this.bb = bb;
    }

    public int getCa() {
        return ca;
    }

    public void setCa(int ca) {
        this.ca = ca;
    }

    public double getBa() {
        return ba;
    }

    public void setBa(double ba) {
        this.ba = ba;
    }

    public boolean isAo() {
        return ao;
    }

    public void setAo(boolean ao) {
        this.ao = ao;
    }
}
